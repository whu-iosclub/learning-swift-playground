/*
## 2. 基本数据类型

Swift 提供了多种基本数据类型，例如：

* `Int`：整数
* `Double` 和 `Float`：浮点数 (Double 精度更高，更常用)
* `Bool`：布尔值 (true / false)
* `String`：字符串
* `Character`：字符

*/

print("\n--- 2. 基本数据类型 ---")

let integer: Int = 10
let float_num: Float = 3.14
let pi_double: Double = 3.1415926535
let isSwiftFun: Bool = true
let greeting: String = "Hello, World!"
let letter: Character = "A"

print("Int: \(integer)")
print("Float: \(float_num)")
print("Double: \(pi_double)")
print("Bool: \(isSwiftFun)")
print("String: \(greeting)")
print("Character: \(letter)")

